print("A Short List of Floating Point Numbers")

floating_point_numbers = [0.1 , 0.2, 0.3]
for f in floating_point_numbers:
    print(f)

print()

print("The Odd Numbers From 99 to 33 in Descending Order")
for z in range (99, 31, -2):
    print(z)

